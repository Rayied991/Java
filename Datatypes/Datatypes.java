package Java.Datatypes;

public class Datatypes {
    public static void main(String[] args) {
        //Primitive Datatype
        byte number=-128;//Its(Byte) minimum value is -128 and maximum value is 127
        System.out.println(number);
        //byte num2=129;//It will occur errors
        byte num2=127;
        System.out.println(num2);

        //Short its minimum value is -32,768 and maximum value is 32,767
        short val=32767;
        System.out.println(val);
        //int its minimum value is -2,147,483 and maximum value is 2,147,483,647
        int v=2147483647;
        System.out.println(v);
        //long its minimum value is -9,223,372,036,854,775,808 and maximum value is -9,223,372,036,854,775,807
        long ln=9223372036854775807L;
        System.out.println(ln);
        float f=12.34f;
        System.out.println(f);
        double d=2.9999;
        System.out.println(d);

       

    }
}
